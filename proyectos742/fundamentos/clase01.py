print("HOLA MUNDO DESDE PYTHON")
# variable de tipo cadena
nombre="bruno diaz"
# variable de tipo entero
edad= 30
# variable de tipo flotante
altura=1.75
# variable de tipo booleano
es_estudienate=True

# imprimir las variables 
print("impresion tradicional")
print("----------------------")
print("nombre", nombre)
print("edad", edad)
print("altura", altura)
print("es_estudienate", es_estudienate)

# imprecion tradicional
print("impresion moderna con f-strings")
print("----------------------")
print(f"nombre: {nombre}")
print(f"edad:{edad}")
print(f"altura:{altura}")
print(f"es_estudienate:{es_estudienate}")